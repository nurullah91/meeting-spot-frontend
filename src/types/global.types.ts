import { BaseQueryApi } from "@reduxjs/toolkit/query";
import { TUser } from "./user.types";

export type TQueryParam = {
  name: string;
  value: boolean | React.Key;
};
export type TError = {
  data: {
    message: string;
    stack: string;
    success: boolean;
  };
  status: number;
};

export type TMeta = {
  limit: number;
  page: number;
  total: number;
  totalPage: number;
};

export type TResponse<T> = {
  data?: T;
  error?: TError;
  meta?: TMeta;
  success: boolean;
  message: string;
};

export type TResponseRedux<T> = TResponse<T> & BaseQueryApi;

export type TLoginData = {
  data: TUser;
  message: string;
  success: boolean;
  token: string;
  statusCode: number;
};
